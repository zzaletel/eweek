using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.IO;
using System.Data;
using System.Drawing;
using System.Configuration;

namespace ASPWenFormPractice1
{
    public partial class TeamTable : System.Web.UI.Page
    {
        protected void Page_Load(object sender, EventArgs e)
        {
            if (HttpContext.Current.User.Identity.IsAuthenticated)
            {
                this.Visible = true;
            }
        }

        protected void ExportToExcel(object sender, EventArgs e)
        {
            if (this.ButtonEdit.Text != "Edit")
            {
                this.Label1.Text = "Cancel Editing State Before Exporting!";
                return;
            }
            Response.Clear();
            Response.Buffer = true;
            Response.AddHeader("content-disposition", "attachment;filename=Team_Table.xls");
            Response.Charset = "";
            Response.ContentType = "application/vnd.ms-excel";
            using (StringWriter sw = new StringWriter())
            {
                HtmlTextWriter hw = new HtmlTextWriter(sw);

                //To Export all pages
                GridView3.AllowPaging = false;
                //this.BindGrid();

                GridView3.HeaderRow.BackColor = Color.White;
                foreach (TableCell cell in GridView3.HeaderRow.Cells)
                {
                    cell.BackColor = GridView3.HeaderStyle.BackColor;
                }
                foreach (GridViewRow row in GridView3.Rows)
                {
                    row.BackColor = Color.White;
                    foreach (TableCell cell in row.Cells)
                    {
                        if (row.RowIndex % 2 == 0)
                        {
                            cell.BackColor = GridView3.AlternatingRowStyle.BackColor;
                        }
                        else
                        {
                            cell.BackColor = GridView3.RowStyle.BackColor;
                        }
                        cell.CssClass = "textmode";
                    }
                }

                GridView3.RenderControl(hw);

                //style to format numbers to string
                string style = @"<style> .textmode { } </style>";
                Response.Write(style);
                Response.Output.Write(sw.ToString());
                Response.Flush();
                Response.End();
            }
        }

        public override void VerifyRenderingInServerForm(Control control)
        {
            /* Verifies that the control is rendered */
        }

        protected void ButtonEdit_Click(object sender, EventArgs e)
        {
           
            if (this.ButtonEdit.Text == "Edit")
            {
                this.GridView3.AutoGenerateDeleteButton = true; //set it back to allow editing/deleting
                this.GridView3.AutoGenerateEditButton = true;
                this.ButtonEdit.Text = "Cancel";
            }
            else {
                this.GridView3.AutoGenerateDeleteButton = false; //set to false to allow exporting
                this.GridView3.AutoGenerateEditButton = false;
                this.ButtonEdit.Text = "Edit";
            }
        
        }
    }
}